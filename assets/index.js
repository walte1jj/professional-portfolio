function navigate(input) {
    var input = input;

    if (input == "taskinator") {
        window.open("https://walte1jj.github.io/Taskinator/");
    } else if (input == "work-day-scheduler") {
        window.open("https://walte1jj.github.io/Work-Day-Scheduler/");
    } else if (input == "run-buddy") {
        window.open("https://walte1jj.github.io/run-buddy/");
    } else if (input == "horiseon") {
        window.open("https://walte1jj.github.io/Horiseon/");
    } else if (input == "taskmaster-pro") {
        window.open("https://walte1jj.github.io/taskmaster-pro/"); 
    } else {
        return true;
    }
};